// HW3_5 (Unary Operation)
public class HW3_5 {
	public static void main (String[] arg) {
		
		// 1. Define a variables and increment the variable value using unary operation (+)
		int numOne = 1;
		numOne++;
		System.out.println("Value of numOne after increment operation: " + numOne);
		
		// 2. Define a variables and decrease the variable value using unary operation (-)					
		int numTwo = 2;
		--numTwo;
		System.out.println("Value of numTwo after increment operation: " + numTwo);
		
		// 3. Define a boolean variable. Invert the boolean value and print the value.
		boolean bFact = true;
		bFact = !bFact;
		System.out.println("Value of bFact after inverse operation: " + bFact);
		
	}
}
