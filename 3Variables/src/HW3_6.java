// HW3_6 (Equality Operation)
public class HW3_6 {
	public static void main(String[] arg) {
		
		// 1. Define a 2 variables and print if first number is greater than second
		int numOne = 5;
		int numTwo = 3;
		
		if (numOne > numTwo)
			System.out.println(numOne + " is greater than " + numTwo);
		
		// 2. Define a 2 variables and print if first number is less than second
		int numThree = 2;
		int numFour = 7;
		
		if (numThree < numFour)
			System.out.println(numThree + " is less than " + numFour);
		
		// 3. Define a 2 variables and print if they are equal
		int numFive = 9;
		int numSix = 9;
		
		if (numFive == numSix)
			System.out.println(numFive + " is equal to " + numSix);
	}
}
